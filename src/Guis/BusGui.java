    private void btnRegistrarBusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarBusActionPerformed
        String placa = txtPlaca.getText();
        String tipoBus = txtTipoBus.getText();
        int cantidadSillas = Integer.valueOf(txtCantidadSillas.getText());
        boolean soat= false;
        if (radioSoatSi.isSelected()) {
            soat = true;
        } else if (radioSoatNo.isSelected()) {
            soat = false;
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar el estado del soat");
        }
        boolean tecnomecanica= false;
        if (radioTecnoSi.isSelected()) {
            tecnomecanica = true;
        } else if (radioTecnoNo.isSelected()) {
            tecnomecanica = false;
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar el estado del soat");
        }
        buses objBuses= new buses(placa, tipoBus, cantidadSillas, soat, tecnomecanica);
        objBuses.setObjBuses(objBuses);
        objBuses.recorrerBuses();
        JOptionPane.showMessageDialog(this, "REGISTRO EXITOSO");
        Limpiar();
    }//GEN-LAST:event_btnRegistrarBusActionPerformed

