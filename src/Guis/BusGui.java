    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String placa = JOptionPane.showInputDialog(this, "Ingrese el NIT");
        buses objBuses2 = new buses();
        buses buscaBus[] = objBuses2.getObjBuses();
        for (int i = 0; i < buscaBus.length; i++) {
            if (buscaBus[i].getPlaca().equals(placa)) {
                txtPlaca.setText(buscaBus[i].getPlaca());
                txtCantidadSillas.setText(Integer.toString(buscaBus[i].getCantidadSillas()));
                txtTipoBus.setText(buscaBus[i].getTipoBus());
                if(buscaBus[i].isSoat()){
                   radioSoatSi.setSelected(true);
                }else if(!(buscaBus[i].isSoat())){
                    radioSoatNo.setSelected(true);
                }
                if(buscaBus[i].isTecnomecanica()){
                   radioTecnoSi.setSelected(true);
                }else if(!(buscaBus[i].isTecnomecanica())){
                    radioTecnoNo.setSelected(true);
                }
                break;
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed
    public void Limpiar(){
        txtCantidadSillas.setText("");
        txtPlaca.setText("");
        txtTipoBus.setText("");
        btGroupSoat.clearSelection();
        btGroupTecno.clearSelection();
    }
