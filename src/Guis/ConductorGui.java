    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String cedula = JOptionPane.showInputDialog(this, "Ingrese el NIT");
        Conductor objConductor2 = new Conductor();
        Conductor[] buscarConductor = objConductor2.getObjConductor();
        for (int i = 0; i < buscarConductor.length; i++) {
            if (buscarConductor[i].getCedulaPersona().equals(cedula)) {
                txtNombre.setText(buscarConductor[i].getNombrePersona());
                txtApellido.setText(buscarConductor[i].getApellidoPersona());
                txtCedula.setText(buscarConductor[i].getCedulaPersona());
                txtCorreo.setText(buscarConductor[i].getCorreoPersona());
                txtDireccion.setText(buscarConductor[i].getDireccionPersona());
                txtFecha.setText(buscarConductor[i].getFechaNacimientoPersonaDate());
                txtSalario.setText(Double.toString(buscarConductor[i].getSalarioConductor()));
                txtTelefono.setText(buscarConductor[i].getTelefono());
                cbContrato.setSelectedItem(buscarConductor[i].getTipoContratoConductor());
                cbLicencia.setSelectedItem(buscarConductor[i].getTipoLicenciaConductor());
                cbRh.setSelectedItem(buscarConductor[i].getRHConductor());
                break;
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed
    public void limpiar() {
        txtNombre.setText("");
                txtApellido.setText("");
                txtCedula.setText("");
                txtCorreo.setText("");
                txtDireccion.setText("");
                txtFecha.setText("");
                txtSalario.setText("");
                txtTelefono.setText("");
                cbContrato.setSelectedIndex(0);
                cbLicencia.setSelectedIndex(0);
                cbRh.setSelectedIndex(0);
    }

