    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String CedulaPersona = txtCedula.getText();
        String nombrePersona = txtNombre.getText();
        String ApellidoPersona = txtApellido.getText();
        String Telefono = txtTelefono.getText();
        String correoPersona = txtCorreo.getText();
        String direccionPersona = txtDireccion.getText();
        String fechaNacimientoPersona = txtFecha.getText();
        double salarioConductor = Double.parseDouble(txtSalario.getText());
        String tipoLicenciaConductor = cbLicencia.getSelectedItem().toString();
        String RHConductor = cbRh.getSelectedItem().toString();

        String tipoContratoConductor = cbRh.getSelectedItem().toString();

        Conductor objConductor = new Conductor(salarioConductor, tipoLicenciaConductor, RHConductor, tipoContratoConductor, CedulaPersona, nombrePersona, ApellidoPersona, Telefono, correoPersona, direccionPersona, fechaNacimientoPersona);
        System.out.println("fecha" + objConductor.getFechaNacimientoPersonaDate() + " cbrh" + objConductor.getRHConductor());
        objConductor.setObjConductor(objConductor);
        JOptionPane.showMessageDialog(this, "REGISTRO EXITOSO");
        limpiar();

    }//GEN-LAST:event_btnRegistrarActionPerformed

