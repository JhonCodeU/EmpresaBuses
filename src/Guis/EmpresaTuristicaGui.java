    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nit = JOptionPane.showInputDialog(this, "Ingrese el NIT");
        EmpresaTuristica objEmpresa2 = new EmpresaTuristica();
        EmpresaTuristica buscaEmpresa[] = objEmpresa2.enviarVector();
        for (int i = 0; i < buscaEmpresa.length; i++) {
            if (buscaEmpresa[i].getNitEmepresa().equals(nit)) {
                txtNit.setText(buscaEmpresa[i].getNitEmepresa());
                txtNombre.setText(buscaEmpresa[i].getNombreEmpresa());
                txtDireccion.setText(buscaEmpresa[i].getDireccionEmpresa());
                txtTelefono.setText(buscaEmpresa[i].getTelefonoEmpresa());
                break;
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

