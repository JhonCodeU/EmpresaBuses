    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nitEmepresa = txtNit.getText();
        String nombreEmpresa = txtNombre.getText();
        String direccionEmpresa = txtDireccion.getText();
        String telefonoEmpresa = txtTelefono.getText();
        EmpresaTuristica objEmpresa = new EmpresaTuristica(nitEmepresa, nombreEmpresa, direccionEmpresa, telefonoEmpresa);
        objEmpresa.registrarEmpresa(objEmpresa);
        
        Limpiar();
    }//GEN-LAST:event_btnRegistrarActionPerformed

