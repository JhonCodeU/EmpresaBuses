            buSillas.getSilla()[Numero-1].setDisponibilidad(false);
            buSillas.getSilla()[Numero - 1].setPasajero(pasajero);
            System.out.println(buSillas);
            System.out.println(pasajero);
            txtNombre.setText("");
            txtCedula.setText("");
            jBoxClase.setSelectedIndex(0);
