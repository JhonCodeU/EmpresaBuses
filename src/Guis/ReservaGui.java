    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String cedula = JOptionPane.showInputDialog(this, "Ingrese el NIT");
        boolean bandera=false;
        Pasajero objPasajero2 = new Pasajero();
        Pasajero[] buscarPasajero = objPasajero2.getObjPasajero();
        for (int i = 0; i < buscarPasajero.length; i++) {
            if (buscarPasajero[i].getCedulaPersona().equals(cedula)) {
                txtNombre.setText(buscarPasajero[i].getNombrePersona());
                txtCedula.setText(buscarPasajero[i].getCedulaPersona());
                lbEscogerBus.setEnabled(true);
                cbBus.setEnabled(true);
                bandera=true;
                break;
            }
        }
        if(!bandera){
            JOptionPane.showMessageDialog(this, "Cliente no Encontrado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

